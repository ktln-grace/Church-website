            <h3> Our</h3>
            <h1> Statement of Faith </h1>
            <p> We Believe . . . <br> <br>
                1. The Scriptures, both Old and New Testaments, to be the inspired Word of God, without error in the original writing, the complete revelation of His will for the salvation of men, and the Divine and final authority for all Christian faith and life. <br> <br>
                2. In one God, Creator of all things, infinitely perfect and eternally existing in three persons, Father, Son and Holy Spirit. <br> <br>
                3. That Jesus Christ is true God and true man, having been conceived of the Holy Spirit and born of the virgin Mary. He died on the Cross a sacrifice for our sins according to the Scriptures. Further, He arose bodily from the dead, ascended into heaven, where, at the right hand of the Majesty on High He now is our High Priest and advocate. <br> <br>
                4. That the ministry of the Holy Spirit is to glorify the Lord Jesus Christ, and during this age, to convict men, regenerate the believing sinner, indwell, guide, instruct, and empower the believer for Godly living and service. <br> <br>
                5. That man was created in the image of God, but fell into sin and is therefore lost and only through regeneration by the Holy Spirit can salvation and spiritual life be obtained. <br> <br>
                6. That the shed blood of Jesus Christ and His resurrection provide the only ground for justification and salvation for all who believe, and only such as receive Jesus Christ are born of the Holy Spirit, and thus become Children of God. <br> <br>
                7. That water baptism and the Lord’s Supper are ordinances to be observed by the church during the present age. They are, however, not to be regarded as a means of salvation. <br> <br>
                8. That the true Church is composed of all such persons, who, through saving faith in Jesus, have been regenerated by the Holy Spirit and are united together in the body of Christ of which He is the head. <br> <br>
                9. That only those who are thus members of the true Church shall be eligible for membership in the local church. <br> <br>
                10. That Jesus Christ is the Lord and Head of the church, and that every local church has the right under Christ to decide and govern its own affairs. <br> <br>
                11. In the personal and pre-millennial and imminent coming of our Lord Jesus Christ and that this “Blessed Hope” has a vital bearing on the personal life and service of the believer. <br> <br>
                12. In the bodily resurrection of the dead; of the believer to everlasting blessedness and joy with the Lord, of the unbeliever to judgment and everlasting conscious punishment. <br> <br>
</p>
